# Variables in Python
x = 10  # This is an integer variable
y = 3.14  # This is a float variable
name = "Alice"  # This is a string variable
is_student = True  # This is a boolean variable
# Printing variables
print("The value of x is:", x)
print("The value of y is:", y)
print("The name is:", name)
print("Is the person a student?", is_student)
# Basic arithmetic operations
sum_result = x + y
difference = x - y
product = x * y
quotient = x / y
print("The sum of x and y is:", sum_result)
print("The difference between x and y is:", difference)
print("The product of x and y is:", product)
print("The quotient of x and y is:", quotient)
# Conditional statements
if x > y:
    print("x is greater than y")
elif x < y:
    print("x is less than y")
else:
    print("x is equal to y")